class Diffusion:
    def __init__(self, noise_steps=1000, beta_start=1e-4, beta_end=0.02, device="cuda"):
        self.noise_steps = noise_steps
        self.beta_start = beta_start
        self.beta_end = beta_end
        self.device = device

        self.beta = self.prepare_noise_schedule().to(device)
        self.alpha = 1. - self.beta
        self.alpha_hat = torch.cumprod(self.alpha, dim=0).to(device)

    def prepare_noise_schedule(self):
        return torch.linspace(self.beta_start, self.beta_end, self.noise_steps)

    def noise_images(self, x, t):
        sqrt_alpha_hat = torch.sqrt(self.alpha_hat[t])[:, None, None, None]
        sqrt_one_minus_alpha_hat = torch.sqrt(1 - self.alpha_hat[t])[:, None, None, None]
        e = torch.randn_like(x)
        return sqrt_alpha_hat * x + sqrt_one_minus_alpha_hat * e, e

    def sample_timesteps(self, n):
#         print(n, self.noise_steps)
        return torch.randint(low=1, high=self.noise_steps, size=(n,))

    def sample(self, model, n):
        model.eval()
        with torch.no_grad():
            x = torch.randn((n, 3, image_size, image_size)).to(self.device)
            for i in reversed(range(1, self.noise_steps)):
                t = (torch.ones(n) * i).long().to(self.device)
                predicted_noise = model(x, t)
                alpha = self.alpha[t][:, None, None, None]
                alpha_hat = self.alpha_hat[t][:, None, None, None]
                beta = self.beta[t][:, None, None, None]
                if i > 1:
                    noise = torch.randn_like(x)
                else:
                    noise = torch.zeros_like(x)
                x = 1 / torch.sqrt(alpha) * (x - ((1 - alpha) / (torch.sqrt(1 - alpha_hat))) * predicted_noise) + torch.sqrt(beta) * noise
        model.train()
        x = (x.clamp(-1, 1) + 1) / 2
        x = (x * 255).type(torch.uint8)
        return x
    
    def sample_interpolation(self, model, samples, steps=8, timesteps=None):
        model.eval()
        assert samples.shape[0] % 2 == 0
        if timesteps == None:
            timesteps = self.noise_steps - 1
        assert samples.shape[0] * steps % 8 == 0
        n = int(samples.shape[0] // 2)
        samples = samples.float()
        samples = ((samples/255)*2) - 1
        with torch.no_grad():
            interp = torch.zeros((n*steps, 3, image_size, image_size)).to(self.device)
            t = (torch.ones(n*2)*(timesteps)).long().to(self.device)
            xnoise,_ = self.noise_images(samples, t)
            for i in range(n):
                for j in range(steps):
                    lam = (j/(steps))
#                     print(i, n, j, i*n+j, interp.shape)
                    interp[i*steps+j] = (xnoise[2*i] * (1-lam)) + (xnoise[2*i+1] * lam)
            last_time = time.time()
            print(interp.shape)
            complete = 0
            out = None
            while complete < interp.shape[0]:
                x = interp[complete:complete+mini_batch_size].to(self.device)
                for i in reversed(range(1, timesteps)):
                    time_remaining = int((time.time() - last_time) * i)
                    last_time = time.time()
                    print(f"[INTERPOLATING] Batch {complete}/{interp.shape[0]} Timestep {i}/{timesteps} Time remaining: {math.floor(time_remaining/60)} mins {time_remaining % 60} seconds", end="\r")
                    t = (torch.ones(n) * i).long().to(self.device)
                    predicted_noise = model(x, t)
                    alpha = self.alpha[t][:, None, None, None]
                    alpha_hat = self.alpha_hat[t][:, None, None, None]
                    beta = self.beta[t][:, None, None, None]
                    if i > 1:
                        noise = torch.randn_like(x)
                    else:
                        noise = torch.zeros_like(x)
                    x = 1 / torch.sqrt(alpha) * (x - ((1 - alpha) / (torch.sqrt(1 - alpha_hat))) * predicted_noise) + torch.sqrt(beta) * noise
                interp[complete:complete+mini_batch_size] = x
